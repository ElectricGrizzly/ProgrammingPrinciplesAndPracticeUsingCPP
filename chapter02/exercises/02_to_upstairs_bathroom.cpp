#include <iostream>

int main()
{
    std::cout << "********INSTRUCTIONS TO UPSTAIRS BATHROOM********" << std::endl;
    std::cout << "1. After entering the front door, turn 90 degree to the left.\n"
        << "2. Take two (2) steps forward through the porch door and turn 90 degrees right.\n"
        << "3. Take five (5) steps forward and turn 90 degree right to face down the hallway.\n"
        << "4. Take seven (7) steps forward down the hallway and turn left 90 degrees.\n"
        << "5. Take two (2) steps forward.\n"
        << "6. You are now in the bathroom.\n";

    return 0;
}
